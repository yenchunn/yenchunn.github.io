        const track = document.getElementById('track'), prevBtn = document.getElementById('prev-btn'), nextBtn = document.getElementById('next-btn');
        let idx = 0; const cards = document.querySelectorAll('.mini-card');
        function updateCarousel() {
            const count = window.innerWidth <= 900 ? 2 : 3;
            track.style.transform = `translateX(-${idx * (cards[0].offsetWidth + 20)}px)`;
        }
        nextBtn.onclick = () => {
            const count = window.innerWidth <= 900 ? 2 : 3;
            idx = (idx < cards.length - count) ? idx + 1 : 0;
            updateCarousel();
        };
        prevBtn.onclick = () => {
            const count = window.innerWidth <= 900 ? 2 : 3;
            idx = (idx > 0) ? idx - 1 : cards.length - count;
            updateCarousel();
        };

